package com.eliid.model;

public class UserMessageInfo {
	private int infoId;
	private String infoName;
	private String infoEmil;
	private String infoBody;
	public int getInfoId() {
		return infoId;
	}
	public void setInfoId(int infoId) {
		this.infoId = infoId;
	}
	public String getInfoName() {
		return infoName;
	}
	public void setInfoName(String infoName) {
		this.infoName = infoName;
	}
	public String getInfoEmil() {
		return infoEmil;
	}
	public void setInfoEmil(String infoEmil) {
		this.infoEmil = infoEmil;
	}
	public String getInfoBody() {
		return infoBody;
	}
	public void setInfoBody(String infoBody) {
		this.infoBody = infoBody;
	}

}
